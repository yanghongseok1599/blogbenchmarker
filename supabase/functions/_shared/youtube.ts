// Track 선택 / fetch
// -----------------------------------------------------------------------------

type CaptionTrack = {
  baseUrl?: string
  languageCode?: string
  kind?: string
  name?: { simpleText?: string }
}

function pickTrack(tracks: CaptionTrack[], prefs: string[]): CaptionTrack | null {
  for (const pref of prefs) {
    const hit = tracks.find(
      (t) => typeof t.languageCode === 'string' && t.languageCode.toLowerCase() === pref.toLowerCase()
    )
    if (hit) return hit
  }
  // 접두사 매칭 (예: 'ko' 으로 'ko-KR' 매치)
  for (const pref of prefs) {
    const hit = tracks.find(
      (t) =>
        typeof t.languageCode === 'string' &&
        t.languageCode.toLowerCase().startsWith(pref.toLowerCase())
    )
    if (hit) return hit
  }
  return tracks[0] ?? null
}

async function fetchSegments(baseUrl: string): Promise<TranscriptSegment[]> {
  const url = appendParam(baseUrl, 'fmt', 'json3')
  let res: Response
  try {
    res = await fetch(url, { headers: { 'User-Agent': USER_AGENT } })
  } catch {
    throw asYoutubeError('upstream_error', '자막 파일을 내려받지 못했습니다.', 502)
  }
  if (!res.ok) {
    throw asYoutubeError('upstream_error', `자막 서버 응답 오류 (HTTP ${res.status}).`, 502)
  }

  const body = await res.text()
  if (!body || body.trim().length === 0) return []

  try {
    const json = JSON.parse(body) as {
      events?: Array<{
        tStartMs?: number
        dDurationMs?: number
        segs?: Array<{ utf8?: string }>
      }>
    }
    const events = json.events ?? []
    const out: TranscriptSegment[] = []
    for (const ev of events) {
      const parts = ev.segs ?? []
      const text = parts
        .map((p) => (typeof p.utf8 === 'string' ? p.utf8 : ''))
        .join('')
        .replace(/\n+/g, ' ')
        .trim()
      if (!text) continue
      out.push({
        text,
        startSec: Math.round(((ev.tStartMs ?? 0) / 1000) * 100) / 100,
        durationSec: Math.round(((ev.dDurationMs ?? 0) / 1000) * 100) / 100
      })
    }
    return out
  } catch {
    throw asYoutubeError('parse_error', '자막 형식을 해석할 수 없습니다.', 502)
  }
}

function appendParam(url: string, key: string, value: string): string {
  const sep = url.includes('?') ? '&' : '?'
  return `${url}${sep}${encodeURIComponent(key)}=${encodeURIComponent(value)}`
}

// -----------------------------------------------------------------------------
// Error helpers
// -----------------------------------------------------------------------------

function asYoutubeError(code: YoutubeError['code'], message: string, status: number): YoutubeError {
  return { code, message, status }
}

export function isYoutubeError(v: unknown): v is YoutubeError {
  return typeof v === 'object' && v !== null && 'code' in v && 'status' in v && 'message' in v
}

export function youtubeErrorResponse(
  err: unknown,
  corsHeaders: Record<string, string> = {}
): Response {
  const e: YoutubeError = isYoutubeError(err)
    ? err
    : { code: 'upstream_error', message: 'YouTube 처리 중 알 수 없는 오류.', status: 500 }
  return new Response(
    JSON.stringify({ ok: false, error: { code: e.code, message: e.message } }),
    { status: e.status, headers: { 'Content-Type': 'application/json', ...corsHeaders } }
  )
}
